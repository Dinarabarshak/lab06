#include <stdio.h>

int main() {
    int n;
    double sum = 0.0;

    printf("Введіть n: ");
    scanf("%d", &n);

    // сума 1/2 + 1/3 + 1/4 + ... + 1/(n+1)
    for (int i = 1; i <= n; i++) {
        sum += 1.0 / (i + 1);
    }

    printf("Сума ряду = %.6f\n", sum);

    return 0;
}
